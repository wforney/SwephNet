namespace Sweph.Net.Chronology;

/// <summary>
/// Represents the calendar system used for date calculations.
/// </summary>
public enum DateCalendar
{
    /// <summary>
    /// The Gregorian calendar, which is the calendar system currently used in most of the world.
    /// </summary>
    Gregorian,

    /// <summary>
    /// The Julian calendar, which was used before the Gregorian calendar was adopted.
    /// </summary>
    Julian,
}
